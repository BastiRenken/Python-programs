print("Text in Morsecode umwandeln")

text = input("Text eingeben: ")
text = text.lower()
text = list(text)
morsecode = []

for i in range(len(text)):
    for j in range(len(zeichenliste)):
        if text[i] == zeichenliste[j][0]:
            morsecode.extend(zeichenliste[j][1])
            morsecode.extend(" ")
            break

